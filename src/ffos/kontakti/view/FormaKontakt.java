    private void lstKontaktValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstKontaktValueChanged
        Kontakt vk;
        try {
            SimpleDateFormat df = new SimpleDateFormat("dd.MM.yyyy");
            df.setLenient(false);

            DefaultListModel m = (DefaultListModel) lstKontakt.getModel();
            vk = (Kontakt) m.get(lstKontakt.getSelectedIndex());
            txtIme.setText(vk.getIme());
            txtPrezime.setText(vk.getPrezime());
            txtBroj.setText(vk.getBroj()+"");
            txtDatum.setText(vk.getDatumUnosa()+ "");

            DefaultComboBoxModel mo = (DefaultComboBoxModel) cmbVrstaKontakta.getModel();
            for (int i = 0; i < mo.getSize(); i++) {
                if (((VrstaKontakta) mo.getElementAt(i)).getId() == vk.getVrstakontakta().getId()) {
                    cmbVrstaKontakta.setSelectedIndex(i);
                    break;
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_lstKontaktValueChanged

