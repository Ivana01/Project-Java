    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        Kontakt vk = new Kontakt();
        SimpleDateFormat df = new SimpleDateFormat("dd.MM.yyyy");
        df.setLenient(false);
        vk.setIme(txtIme.getText());
        vk.setPrezime(txtPrezime.getText());
        vk.setBroj(Integer.parseInt(txtBroj.getText()));
        try {
            vk.setDatumUnosa(df.parse(txtDatum.getText()));
        } catch (ParseException ex) {
            JOptionPane.showConfirmDialog(getRootPane(), "Niste unijeli dobar datum");
        }

        DefaultComboBoxModel m = (DefaultComboBoxModel) cmbVrstaKontakta.getModel();
        vk.setVrstakontakta((VrstaKontakta) m.getElementAt(cmbVrstaKontakta.getSelectedIndex()));
        obrada.spremi(vk);
        ucitaj();
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        Kontakt vk;
        try {
            DefaultListModel m = (DefaultListModel) lstKontakt.getModel();
            vk = (Kontakt) m.get(lstKontakt.getSelectedIndex());
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, "Prvo odaberite stavku");
            return;
        }
        try {
            obrada.obrisi(vk);
        } catch (Exception ex) {
            JOptionPane.showConfirmDialog(rootPane, "Prvo odaberite stavku");
        }

        ucitaj();
    }//GEN-LAST:event_btnObrisiActionPerformed

